let button_1 = document.getElementById('button_1');  
button_1.addEventListener('mouseover', ev => {
	button_1.style.backgroundColor = "rgb(117, 116, 116)";
    let button_a_1 = document.getElementById('button_a_1');
    button_a_1.style.color = "rgb(255, 71, 71)";
});
button_1.addEventListener('mouseout', ev => {
	button_1.style.backgroundColor = "rgb(255, 170, 13)";
    let button_a_1 = document.getElementById('button_a_1');
    button_a_1.style.color = "rgb(0, 0, 0)";
});

